# Update the score of a student in the dictionary.

# Existing dictionary of student scores
student_scores = {
    "Alice": 85,
    "Bob": 92,
    "Charlie": 78
}

# Update Bob's score
student_scores["Bob"] = 95

# Print the updated dictionary
print("Updated student scores:", student_scores)


# Note: student_scores["Bob"] = 95 updates Bob’s score from 92 to 95.

# If the key "Bob" didn’t exist, this line would add it as a new entry.